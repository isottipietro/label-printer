<h2 style="background-color: #c4df9c;">Istruzioni</h2>
<p>Questo strumento permette di stampare etichette per l'identificazione di farmaci</p>
<ol>
  <li>Compila i campi del modulo</li>
  <li>Premi il tasto "Stampa"</li>
  <li>Se necessario, seleziona come stampante la "ZEBRA"</li>
</ol>
<button type="button" class="collapsible">Etichetta singola</button>
<div class="content">
  <p>Questo modulo è dedicato alla stampa delle etichette per l'identificazione dei farmaci, una volta diluiti, da apporre su una siringa o su una flebo.</p>
  <p>Attualmente è necessario compilare tutti i campi manualmente.</p>
  <p>Le opzioni denominate "avvertenze" permettono di evidenziare graficamente preparazioni farmacologiche che richiedono particolare attenzione: in caso di farmaci pericolosi (amine, curari, potassio...) o di diluizioni non standard.</p>
</div>
<button type="button" class="collapsible">Intubazione</button>
<div class="content">
  <p>Questo modulo permette di stampare direttamente tutte le etichette dei farmaci da avere a disposizione durante una procedura di intubazione.</p>
  <p>I campi richiesti sono necessari per l'identificazione del paziente e dell'operatore, l'elenco dei farmaci è definito dalle indicazioni aziendali o linee guida ed è personalizzabile.</p>
</div>
<button type="button" class="collapsible">Urgenza</button>
<div class="content">
  <p>Il tasto "Urgenza" permette di stampare direttamente tutte le etichette dei farmaci necessari in caso di emergenza/urgenza.</p>
  <p>Non è richiesto nessun campo, le etichette vengono generate immediatamente.</p>
  <p>L'elenco dei farmaci è definito dalle indicazioni aziendali o linee guida ed è personalizzabile.</p>
</div>
<h2 style="background-color: #9cc1df;">Domande Frequenti</h2>
<button type="button" class="collapsible">Cosa c'è scritto nelle etichette?</button>
<div class="content">
  <p>A prescindere dal metodo di creazione, il layout dell'etichetta rimane invariato. I dati riportati sono i seguenti:</p>
  <img src="assets/images/esempio.png" style="width: 400px; margin-left: auto; margin-right: auto; display: block;">
  <ol>
    <li>Nome e Cognome del paziente</li>
    <li>Numero di ricovero del paziente, viene riportato anche in formato Barcode-128</li>
    <li>Nome del farmaco</li>
    <li>Concentrazione della soluzione ottenuta</li>
    <li>Diluizione della preparazione, viene riportato il volume totale e il diluente utilizzato</li>
    <li>Firma del preparatore</li>
    <li>Data e ora della preparazione, inserita automaticamente al momento della stampa</li>
  </ol>
</div>
<button type="button" class="collapsible">Perché le etichette non vengono stampate correttamente?</button>
<div class="content">
  <p>Il layout è definito sulla dimensione delle etichette a disposizione nella stampante "ZEBRA", se la stampa non è corretta puoi provare queste soluzioni:</p>
  <ul>
    <li>Seleziona la stampante corretta</li>
    <li>Verifica che la stampante non sia inceppata o spenta</li>
    <li>Verifica che le impostazioni della pagina di stampa siano:</li>
    <ul>
      <li>Layout: Verticale</li>
      <li>Scala: Predefinito</li>
      <li>Grafica in background Selezionato</li>
    </ul>
  </ul>
</div>
